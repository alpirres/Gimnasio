    /**
     * Metodo que lanza un hilo para mostrar un mensaje durante el tiempo estimado
     * @param msg 
     */
    public void throwMsg(JLabel msg){
        Thread thread = new Thread(new Runnable() {
            @Override
            public void run() {
                try {
                    msg.setVisible(true);
                    Thread.sleep(3000);
                    msg.setVisible(false);
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        });
        thread.start();
                
    }

